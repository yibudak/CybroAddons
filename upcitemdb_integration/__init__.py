from odoo import api, SUPERUSER_ID
from . import models


def pre_init_hook(cr):
    """The function will execute during the module installation for activate
    all currencies."""
    env = api.Environment(cr, SUPERUSER_ID, {})
    currencies = env['res.currency'].search([('active', '=', False)])
    currencies.write({'active': True})
